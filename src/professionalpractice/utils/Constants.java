package professionalpractice.utils;

public class Constants {

    /**
     * Código de estado para una operación que se completó con éxito.
     * Generalmente usado para indicar que una transacción con la base de datos fue exitosa.
     */
    public static final int OPERATION_SUCCESFUL = 200;

    /**
     * Código de estado para un error de conexión con la base de datos.
     * Se usa en los bloques catch de las operaciones de base de datos.
     */
    public static final int CONNECTION_FAILED = 500;

}
